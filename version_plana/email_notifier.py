"""
Email Notifier — AI Investment Fund
Sends weekly PDF report via Gmail (free, no subscription needed).

Setup:
  1. Gmail → Settings → Security → 2-Step Verification (enable)
  2. Gmail → Settings → Security → App Passwords → Create one named "InvestFund"
  3. Add to .env:
        EMAIL_SENDER=tumail@gmail.com
        EMAIL_APP_PASSWORD=xxxx xxxx xxxx xxxx   (16-char App Password, no spaces)
        EMAIL_RECIPIENT=tumail@gmail.com           (can be same or different)
"""
import os
import smtplib
import ssl
from email.mime.multipart import MIMEMultipart
from email.mime.text    import MIMEText
from email.mime.base    import MIMEBase
from email              import encoders
from pathlib            import Path
from datetime           import datetime
from dotenv             import load_dotenv

load_dotenv()

SENDER    = os.getenv("EMAIL_SENDER", "")
APP_PASS  = os.getenv("EMAIL_APP_PASSWORD", "")
RECIPIENT = os.getenv("EMAIL_RECIPIENT", "")


def _is_configured() -> bool:
    return bool(SENDER and APP_PASS and RECIPIENT and
                APP_PASS != "your_gmail_app_password_here")


def send_email(subject: str, body_html: str, pdf_path: Path = None) -> bool:
    """Send an HTML email with optional PDF attachment via Gmail."""
    if not _is_configured():
        print("[Email] Not configured — skipping")
        return False

    try:
        msg = MIMEMultipart("mixed")
        msg["From"]    = f"AI Investment Fund <{SENDER}>"
        msg["To"]      = RECIPIENT
        msg["Subject"] = subject

        # HTML body
        msg.attach(MIMEText(body_html, "html"))

        # Attach PDF if provided
        if pdf_path and pdf_path.exists():
            with open(pdf_path, "rb") as f:
                part = MIMEBase("application", "octet-stream")
                part.set_payload(f.read())
            encoders.encode_base64(part)
            part.add_header("Content-Disposition",
                            f"attachment; filename={pdf_path.name}")
            msg.attach(part)

        context = ssl.create_default_context()
        with smtplib.SMTP_SSL("smtp.gmail.com", 465, context=context) as server:
            server.login(SENDER, APP_PASS)
            server.sendmail(SENDER, RECIPIENT, msg.as_string())

        print(f"[Email] Sent: {subject}")
        return True

    except Exception as e:
        print(f"[Email] Error: {e}")
        return False


