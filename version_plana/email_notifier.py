def email_weekly_report(pdf_path: Path, regions: int, picks: int, top_pick: str):
    """Send the weekly PDF report as email attachment."""
    date_str = datetime.now().strftime("%d %B %Y")
    content = f"""
    <div class="card">
      <h3>Weekly Report — {date_str}</h3>
      <p style="color:#94a3b8">Your weekly global investment intelligence report is attached.</p>
      <br>
      <div class="metric"><span>Regions</span><br><b>{regions}/6</b></div>
      <div class="metric"><span>Opportunities</span><br><b>{picks}</b></div>
      <div class="metric"><span>Top Pick</span><br><b>{top_pick}</b></div>
    </div>
    <p style="color:#94a3b8;font-size:13px">
      Open the attached PDF for the full analysis including global markets, 
      commodities, FX rates, real estate opportunities, and business ideas.
    </p>"""

    send_email(
        subject=f"Investment Fund — Weekly Report {date_str}",
        body_html=_base_html(content),
        pdf_path=pdf_path,
    )


def email_buy_alert(ticker: str, company: str, price: float, target: float,
                    mos_pct: float, score: int, reason: str):
    """Send a BUY signal email."""
    potential = round((target - price) / price * 100, 1)
    content = f"""
    <div class="card buy">
      <h3>🟢 BUY SIGNAL — {ticker}</h3>
      <p style="color:#f1f5f9;font-size:15px;font-weight:bold">{company}</p>
      <div class="metric"><span>Entry Price</span><br><b>${price:,.2f}</b></div>
      <div class="metric"><span>12M Target</span><br><b class="green">${target:,.2f} (+{potential}%)</b></div>
      <div class="metric"><span>Margin of Safety</span><br><b class="green">{mos_pct:.0f}%</b></div>
      <div class="metric"><span>Hybrid Score</span><br><b class="gold">{score}/100</b></div>
      <br><br>
      <p style="color:#94a3b8"><b>Investment Thesis:</b> {reason}</p>
    </div>"""
    send_email(f"BUY Signal: {ticker} — ${price:,.2f}", _base_html(content))


def email_sell_alert(ticker: str, company: str, price: float,
                     entry_price: float, gain_pct: float, reason: str):
    """Send a SELL / take-profit email."""
    css_class = "sell"
    color_class = "green" if gain_pct >= 0 else "red"
    content = f"""
    <div class="card {css_class}">
      <h3>{'💚 TAKE PROFIT' if gain_pct >= 0 else '🔴 STOP LOSS'} — {ticker}</h3>
      <p style="color:#f1f5f9;font-size:15px;font-weight:bold">{company}</p>
      <div class="metric"><span>Sell Price</span><br><b>${price:,.2f}</b></div>
      <div class="metric"><span>Entry Price</span><br><b>${entry_price:,.2f}</b></div>
      <div class="metric"><span>Return</span><br><b class="{color_class}">{gain_pct:+.1f}%</b></div>
      <br><br>
      <p style="color:#94a3b8"><b>Reason:</b> {reason}</p>
    </div>"""
    send_email(f"{'SELL' if gain_pct >= 0 else 'STOP'}: {ticker} ({gain_pct:+.1f}%)", _base_html(content))
